void _CC_YUV420P_RGB24_2x2(
	uint32_t width,
	uint32_t height,
	const uint32_t *sstride,
	const uint32_t *dstride,
	const uint8_t *const *sdata,
	uint8_t *const *ddata
){
	for(uint32_t iheight = 0; EXPECT(iheight != height, 1); iheight += 2){
		const uint8_t *sdata00 = sdata[0] + (iheight + 0) * sstride[0];
		const uint8_t *sdata01 = sdata[0] + (iheight + 1) * sstride[0];
		const uint8_t *sdata1 = sdata[1] + (iheight / 2) * sstride[1];
		const uint8_t *sdata2 = sdata[2] + (iheight / 2) * sstride[2];
		uint8_t *ddata00 = ddata[0] + (iheight + 0) * dstride[0];
		uint8_t *ddata01 = ddata[0] + (iheight + 1) * dstride[0];
		uint8_t *ddata01_limit = &ddata01[width * 3];
		while(EXPECT(ddata01 != ddata01_limit, 1)){
			uint8_t u = *sdata1++;
			uint8_t v = *sdata2++;

			sint32_t c1 = _CC_YUV420P_crv_tab[v];
			sint32_t c3 = _CC_YUV420P_cgv_tab[v];
			sint32_t c2 = _CC_YUV420P_cgu_tab[u] + c3;
			sint32_t c4 = _CC_YUV420P_cbu_tab[u];

			for(uint8_t i = 0; i < 2; i++){
				sint32_t y = _CC_YUV420P_tab_76309[*sdata00++];
				*ddata00++ = _CC_YUV420P_clp_center[(y + c1) >> 16];
				*ddata00++ = _CC_YUV420P_clp_center[(y - c2) >> 16];
				*ddata00++ = _CC_YUV420P_clp_center[(y + c4) >> 16];
			}

			for(uint8_t i = 0; i < 2; i++){
				sint32_t y = _CC_YUV420P_tab_76309[*sdata01++];
				*ddata01++ = _CC_YUV420P_clp_center[(y + c1) >> 16];
				*ddata01++ = _CC_YUV420P_clp_center[(y - c2) >> 16];
				*ddata01++ = _CC_YUV420P_clp_center[(y + c4) >> 16];
			}
		}
	}
}
